public class Student {
    private int stID;
    private String stName;
    private int stRoll;
    private long stContact;

    // Student class constractor
    public Student(int stID, String stName, int stRoll, long stContact) {
        this.stID = stID;
        this.stName = stName;
        this.stRoll = stRoll;
        this.stContact = stContact;
    }
}
